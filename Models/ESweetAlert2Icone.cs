namespace SweetAlert2.Blazor.Models;

/// <summary>
/// Classe estática que contém os ícones predefinidos para os alertas do SweetAlert2.
/// Cada valor corresponde a um tipo de ícone usado para exibir diferentes categorias de alertas.
/// </summary>
public static class ESweetAlert2Icone
{
    /// <summary>
    /// Ícone de erro, utilizado para alertas de erro.
    /// </summary>
    public static readonly string ERROR = "error";

    /// <summary>
    /// Ícone informativo, utilizado para alertas de informação.
    /// </summary>
    public static readonly string INFO = "info";

    /// <summary>
    /// Ícone de pergunta, utilizado para alertas que requerem confirmação ou questionamento.
    /// </summary>
    public static readonly string QUESTION = "question";

    /// <summary>
    /// Ícone de sucesso, utilizado para alertas de sucesso.
    /// </summary>
    public static readonly string SUCCESS = "success";

    /// <summary>
    /// Ícone de aviso, utilizado para alertas de advertência.
    /// </summary>
    public static readonly string WARNING = "warning";
}
